#include "GP_TextStyle.h"

#include <stddef.h>

void GP_DefaultTextStyle(struct GP_TextStyle *style)
{
	style->font = NULL;
	style->pixel_xspace = 0;
	style->pixel_yspace = 0;
	style->pixel_xmul = 1;
	style->pixel_ymul = 1;
}
